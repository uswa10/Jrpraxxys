    {
      $this->validate($request,[
      'comments'=>'required',
      ]);

      $comments = new comment;

      $comments->comments = $request->input('comments');
      $comments->post_id = $request->input('post_id');
      $comments->name = Auth::user()->name;
      $comments->created_at = now();

      $comments->save();



      return redirect('profile/show')->with('success','Post Created.');


